function DarkModeToggle(props:any) {
  const [isLight, setIsLight] = useState<boolean>(true);
  useEffect(()=>{
    if(props.onChange){
      props.onChange(isLight)
    }
  },[isLight])
  return (
    <Wrapper onClick={()=> setIsLight(!isLight)}>
      {isLight ? <SunIcon /> : null}
      {!isLight ? <MoonIcon /> : null}
    </Wrapper>
  );
}

